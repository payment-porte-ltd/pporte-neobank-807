function fnUpdatesenderparams(selectedValue) {
    var assetcode = selectedValue;
    if (assetcode == '') {
        swal.fire('Select Destination Coin');
        return false;
    } else {
        //UpdateConversionRate();
        $("#spansendcode").text(assetcode);
        $("#receiver_asset").val(assetcode);

		if(assetcode==="VESL"||assetcode==="vesl"){
			document.getElementById("amount").style.backgroundImage = "url('./assets/images/crypto/vessel.png')";
        }
        if(assetcode==="PORTE" || assetcode==="porte"){
			document.getElementById("amount").style.backgroundImage = "url('./assets/images/crypto/porte.svg')";
        }
        if(assetcode==="XLM"||assetcode==="xlm"){
			document.getElementById("amount").style.backgroundImage = "url('./assets/images/crypto/xlm.svg')";
        }
        if(assetcode==="USDC"||assetcode==="usdc"){
				document.getElementById("amount").style.backgroundImage = "url('./assets/images/crypto/usdc.png')";
        }
    }
    
}

