function fnViewMore(status, txnCode, txnDate ){
    //console.log('Status is ', status);
	$("#date1").html(txnDate);
	$("#date2").html(txnDate);
	$("#txn_code").html(txnCode);
    if(status === 'C'){
        $("#received_money").show();
        $("#money_on_its_way").show();
        $("#final_list").addClass( "active" );
        $("#second_last_list").addClass( "active" );
    }else if(status === 'PT'){
        $("#received_money").hide();
		$("#money_on_its_way").hide();
        $("#final_list").removeClass( "active" );
		$("#second_last_list").removeClass( "active" );
    }else if(status === 'PP'){
		$("#received_money").hide();
		$("#money_on_its_way").show();
        $("#final_list").removeClass( "active" );
		$("#second_last_list").addClass( "active" );
	}
    $('#exampleModal3').modal('show');
}