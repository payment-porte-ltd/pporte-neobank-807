function fnPayAnyoneFiatWallet(){
    console.log('fnPayAnyoneFiatWallet')
	  $( "#pay-anyone-form" ).validate( {
        rules: {
            selregistereduser: {
                required: true,
            },
            amount: {
                required: true
            },
			comment: {
				required: true,
				 maxlength:30
			}
        },
        messages: {
            input_receiver: {
                required: $('#data-validation-email-id').text(),
 				required: $('#data-validation-must-be-email-id').text(),
            },
            amount: {
                required: $('#data-validation-amount-is-required').text(),
                
            },
			comment:{
				required: $('#data-validation-comment-is-required').text(),
				maxlength: $('#data-validation-comment-must-be-30-characters').text(),
			}
        },
        errorElement: "em",
        errorPlacement: function ( error, element ) {
            // Add the `invalid-feedback` class to the error element
            error.addClass( "invalid-feedback" );
            if ( element.prop( "type" ) === "checkbox" ) {
                error.insertAfter( element.next( "label" ) );
            } else {
                error.insertAfter( element );
            }
        },
        highlight: function ( element, errorClass, validClass ) {
            $( element ).addClass( "is-invalid" ).removeClass( "is-valid" );
        },
        unhighlight: function (element, errorClass, validClass) {
            $( element ).addClass( "is-valid" ).removeClass( "is-invalid" );
        }
    });
    if($( "#pay-anyone-form" ).valid()){
        console.log('form is valid')
        $('#pay-anyone-form input[name="qs"]').val('wal');
        $('#pay-anyone-form input[name="rules"]').val('fiatwalletp2p');
        $('#pay-anyone-form input[name="hdnselregistereduser"]').val($('#selregistereduser :selected').val());
        var formData = new FormData($('#pay-anyone-form')[0]);
        formData.append("hdnlang", $('#lang_def').text());
        for (var pair of formData.entries()) {
          console.log(pair[0] + " - " + pair[1]);
        }	
        $.ajaxSetup({
            beforeSend: function(xhr) {
                xhr.setRequestHeader('x-api-key' , getAPIKey());
            }
        }); 
        $.ajax({
            url: 'ms',
            data: formData,
            processData: false,
            contentType: false,
            type: 'POST',
            success: function (result) {
                
                var data = JSON.parse(result);
                console.log('data ',data);
                if(data.error=='false'){
                    Swal.fire({
                        icon: 'success',
                        text: data.message,
                        showConfirmButton: true,
                        confirmButtonText: "Ok",
                    }).then(function() {
                        $('#post-form').attr('action', 'ws');
                        $('input[name="qs"]').val('wal');
                        $('input[name="rules"]').val('Cash Transactions');
                        $("#post-form").submit();
                    });	
                }else{
                    Swal.fire({
                        text: data.message, 
                        icon: "error",
                        showConfirmButton: true,
                        confirmButtonText: "Ok",
                        }).then(function() {
                            
                        });	
                }
            },
            error: function() {
                Swal.fire({
                            icon: 'error',
                            title: $('#data-validation-error-swal-header').text(),
                            text: $('#data-validation-problem-with-connection').text(),
                            showConfirmButton: true,
                            confirmButtonText: "Ok",
                            }).then(function() {
                                
                    });
            }
        });              	
    }
}
