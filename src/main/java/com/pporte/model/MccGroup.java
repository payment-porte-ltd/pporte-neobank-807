package com.pporte.model;

public class MccGroup {
	private String mccId;
	private String mccName;
	private String mccFromRange;
	private String mccToRange;
	private String mccGeneric;
	private String createdon;
	
	public String getMccId() {
		return mccId;
	}
	public void setMccId(String mccId) {
		this.mccId = mccId;
	}
	public String getMccName() {
		return mccName;
	}
	public void setMccName(String mccName) {
		this.mccName = mccName;
	}
	public String getMccFromRange() {
		return mccFromRange;
	}
	public void setMccFromRange(String mccFromRange) {
		this.mccFromRange = mccFromRange;
	}
	public String getMccToRange() {
		return mccToRange;
	}
	public void setMccToRange(String mccToRange) {
		this.mccToRange = mccToRange;
	}
	public String getMccGeneric() {
		return mccGeneric;
	}
	public void setMccGeneric(String mccGeneric) {
		this.mccGeneric = mccGeneric;
	}
	public String getCreatedon() {
		return createdon;
	}
	public void setCreatedon(String createdon) {
		this.createdon = createdon;
	}

	
	
}
