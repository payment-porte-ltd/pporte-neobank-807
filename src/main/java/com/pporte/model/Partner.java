package com.pporte.model;

public class Partner {
	private String userId;
	private String parnerName;
	private String parnerEmail;
	private String parnerContact;
	private String parnerLocation;
	private String parnerCurrency;
	private String parnerPublicKey;
	private String status;
	private String createdOn;
	
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getParnerName() {
		return parnerName;
	}
	public void setParnerName(String parnerName) {
		this.parnerName = parnerName;
	}
	public String getParnerEmail() {
		return parnerEmail;
	}
	public void setParnerEmail(String parnerEmail) {
		this.parnerEmail = parnerEmail;
	}
	public String getParnerContact() {
		return parnerContact;
	}
	public void setParnerContact(String parnerContact) {
		this.parnerContact = parnerContact;
	}
	public String getParnerLocation() {
		return parnerLocation;
	}
	public void setParnerLocation(String parnerLocation) {
		this.parnerLocation = parnerLocation;
	}
	public String getParnerCurrency() {
		return parnerCurrency;
	}
	public void setParnerCurrency(String parnerCurrency) {
		this.parnerCurrency = parnerCurrency;
	}
	public String getParnerPublicKey() {
		return parnerPublicKey;
	}
	public void setParnerPublicKey(String parnerPublicKey) {
		this.parnerPublicKey = parnerPublicKey;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getCreatedOn() {
		return createdOn;
	}
	public void setCreatedOn(String createdOn) {
		this.createdOn = createdOn;
	}
	
	

}
