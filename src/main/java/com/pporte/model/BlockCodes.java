package com.pporte.model;


public class BlockCodes {
	
	private String blockCodeId;
	private String blockCodeDesc;
	private String status;
	private String authAction;
	private String createdOn;
	
	public String getBlockCodeId() {
		return blockCodeId;
	}
	public void setBlockCodeId(String blockCodeId) {
		this.blockCodeId = blockCodeId;
	}
	public String getBlockCodeDesc() {
		return blockCodeDesc;
	}
	public void setBlockCodeDesc(String blockCodeDesc) {
		this.blockCodeDesc = blockCodeDesc;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getAuthAction() {
		return authAction;
	}
	public void setAuthAction(String authAction) {
		this.authAction = authAction;
	}
	public String getCreatedOn() {
		return createdOn;
	}
	public void setCreatedOn(String createdOn) {
		this.createdOn = createdOn;
	}


}
